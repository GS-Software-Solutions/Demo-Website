#!/usr/bin/env node
// Run this script to extract random profile data from the original index.html:
//   node extract-profiles.js
//
// It will write src/data/random-profiles.ts with all 17 languages.

const fs = require('fs');
const path = require('path');

const html = fs.readFileSync(path.join(__dirname, '..', 'index.html'), 'utf8');

// Find the RANDOM_CUSTOMERS_ALL object
const startMarker = 'const RANDOM_CUSTOMERS_ALL = ';
const startIdx = html.indexOf(startMarker);
if (startIdx === -1) { console.error('Could not find RANDOM_CUSTOMERS_ALL in index.html'); process.exit(1); }

const braceStart = html.indexOf('{', startIdx);
let depth = 0;
let end = braceStart;
for (let i = braceStart; i < html.length; i++) {
  if (html[i] === '{') depth++;
  else if (html[i] === '}') {
    depth--;
    if (depth === 0) { end = i + 1; break; }
  }
}

const jsonStr = html.substring(braceStart, end);
const data = JSON.parse(jsonStr);

let out = '// Auto-extracted random profile data from index.html\n';
out += '// Generated by: node extract-profiles.js\n';
out += '// eslint-disable-next-line @typescript-eslint/no-explicit-any\n';
out += 'export const RANDOM_CUSTOMERS: Record<string, any[]> = ';
out += JSON.stringify(data.CUSTOMERS, null, 2) + ';\n\n';
out += '// eslint-disable-next-line @typescript-eslint/no-explicit-any\n';
out += 'export const RANDOM_MODERATORS: Record<string, any[]> = ';
out += JSON.stringify(data.MODERATORS, null, 2) + ';\n';

const outPath = path.join(__dirname, 'src', 'data', 'random-profiles.ts');
fs.writeFileSync(outPath, out);
console.log('Written ' + out.length + ' bytes to ' + outPath);
